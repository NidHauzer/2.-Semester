
public class Employee {
    private String name;
    private int employeeNo;

    // Constructor
    public Employee(String name, int employeeNo) {
        this.name = name;
        this.employeeNo = employeeNo;
    }

    // Getter for name
    public String getName() {
        return name;
    }

    // Getter for employeeNo
    public int getEmployeeNo() {
        return employeeNo;
    }
}